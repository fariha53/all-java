package begineerjava;

public class AssignmentOperator {

    public static void main(String[] args) {
	// write your code here
        int x= 3;
        int y= 2;
        x+=y;// x +y = x = 5
        System.out.println("x ="+x);
        x-=y;// x -y = x = 5-2= 3
        System.out.println("x ="+x);
        x*=y;// x *y = x = 3*2=6
        System.out.println("x ="+x);
        x/=y;// x /y = x = 6/2= 3
        System.out.println("x ="+x);
        x%=y;// x %y = x = 3%2=1
        System.out.println("x ="+x);


    }
}
